		private bool LoadFunction<T>(ref T func, string name)
		{
			var ptr = _glProvider(name);
			if (ptr == IntPtr.Zero) return false;
			func = Marshal.GetDelegateForFunctionPointer<T>(ptr);
			return true;
		}
		
